local keymap = vim.keymap -- for conciseness

keymap.set("v", "J", ":m '>+1<CR>gv=gv")
keymap.set("v", "K", ":m '<-2<CR>gv=gv")

vim.keymap.set("n", "<leader>cd", function()
  require("trouble").toggle("diagnostics")
end, { desc = "Show diagnostics" })
